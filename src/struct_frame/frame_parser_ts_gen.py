        yield f'                this.msg_id = byte;\n'
        
        if fmt.has_length:
            yield f'                this.state = {class_name}ParserState.GETTING_LENGTH;\n'
        else:
            yield f'                if (this.get_msg_length) {{\n'
            yield f'                    const msg_length = this.get_msg_length(byte);\n'
            yield f'                    if (msg_length !== undefined) {{\n'
            yield f'                        this.packet_size = {class_name}.OVERHEAD + msg_length;\n'
            yield f'                        this.state = {class_name}ParserState.GETTING_PAYLOAD;\n'
            yield f'                    }} else {{\n'
            reset_state = 'LOOKING_FOR_START1' if len(fmt.start_bytes) > 1 else ('LOOKING_FOR_START' if fmt.start_bytes else 'GETTING_MSG_ID')
            yield f'                        this.state = {class_name}ParserState.{reset_state};\n'
            yield f'                    }}\n'
            yield f'                }} else {{\n'
            yield f'                    this.state = {class_name}ParserState.{reset_state};\n'
            yield f'                }}\n'
        yield f'                break;\n\n'
        
        # GETTING_LENGTH state (if applicable)
        if fmt.has_length:
            yield f'            case {class_name}ParserState.GETTING_LENGTH:\n'
            yield f'                this.buffer.push(byte);\n'
            if fmt.length_bytes == 1:
                yield f'                this.msg_length = byte;\n'
                yield f'                this.packet_size = {class_name}.OVERHEAD + this.msg_length;\n'
                yield f'                this.state = {class_name}ParserState.GETTING_PAYLOAD;\n'
            else:
                yield f'                if (this.buffer.length === {len(fmt.start_bytes) + 2}) {{\n'
                yield f'                    this.length_lo = byte;\n'
                yield f'                }} else {{\n'
                yield f'                    this.msg_length = this.length_lo | (byte << 8);\n'
                yield f'                    this.packet_size = {class_name}.OVERHEAD + this.msg_length;\n'
                yield f'                    this.state = {class_name}ParserState.GETTING_PAYLOAD;\n'
                yield f'                }}\n'
            yield f'                break;\n\n'
        
        # GETTING_PAYLOAD state
        yield f'            case {class_name}ParserState.GETTING_PAYLOAD:\n'
        yield f'                this.buffer.push(byte);\n\n'
        yield f'                if (this.buffer.length >= this.packet_size) {{\n'
        
        if fmt.has_crc:
            yield f'                    const msg_length = this.packet_size - {class_name}.OVERHEAD;\n'
            crc_start = len(fmt.start_bytes)
            crc_len = f'msg_length + 1'
            if fmt.has_length:
                crc_len += f' + {fmt.length_bytes}'
            yield f'                    const ck = fletcher_checksum(this.buffer, {crc_start}, {crc_start} + {crc_len});\n'
            yield f'                    if (ck[0] === this.buffer[this.buffer.length - 2] && ck[1] === this.buffer[this.buffer.length - 1]) {{\n'
            yield f'                        result.valid = true;\n'
            yield f'                        result.msg_id = this.msg_id;\n'
            yield f'                        result.msg_len = msg_length;\n'
            yield f'                        result.msg_data = new Uint8Array(this.buffer.slice({class_name}.HEADER_SIZE, this.packet_size - {class_name}.FOOTER_SIZE));\n'
            yield f'                    }}\n'
        else:
            yield f'                    result.valid = true;\n'
            yield f'                    result.msg_id = this.msg_id;\n'
            yield f'                    result.msg_len = this.packet_size - {class_name}.OVERHEAD;\n'
            yield f'                    result.msg_data = new Uint8Array(this.buffer.slice({class_name}.HEADER_SIZE));\n'
        
        reset_state = 'LOOKING_FOR_START1' if len(fmt.start_bytes) > 1 else ('LOOKING_FOR_START' if fmt.start_bytes else 'GETTING_MSG_ID')
        yield f'                    this.state = {class_name}ParserState.{reset_state};\n'
        yield f'                }}\n'
        yield f'                break;\n'
        
        yield f'        }}\n\n'
        yield f'        return result;\n'
        yield f'    }}\n\n'
        
        # Static encode method
        yield f'    static encode(msg_id, msg) {{\n'
        yield f'        const output = [];\n'
        
        for i, (sb_name, sb_value) in enumerate(fmt.start_bytes):
            const_name = f'{class_name}.START_BYTE{i + 1}' if len(fmt.start_bytes) > 1 else f'{class_name}.START_BYTE'
            yield f'        output.push({const_name});\n'
        yield f'        output.push(msg_id);\n'
        
        if fmt.has_length:
            if fmt.length_bytes == 1:
                yield f'        output.push(msg.length & 0xFF);\n'
            else:
                yield f'        output.push(msg.length & 0xFF);\n'
                yield f'        output.push((msg.length >> 8) & 0xFF);\n'
        
        yield f'        for (let i = 0; i < msg.length; i++) {{\n'
        yield f'            output.push(msg[i]);\n'
        yield f'        }}\n'
        
        if fmt.has_crc:
            crc_start = len(fmt.start_bytes)
            crc_len = 'msg.length + 1'
            if fmt.has_length:
                crc_len += f' + {fmt.length_bytes}'
            yield f'        const ck = fletcher_checksum(output, {crc_start}, {crc_start} + {crc_len});\n'
            yield f'        output.push(ck[0]);\n'
            yield f'        output.push(ck[1]);\n'
        
        yield f'        return new Uint8Array(output);\n'
        yield f'    }}\n\n'
        
        # Static validate_packet method
        yield f'    static validate_packet(buffer) {{\n'
        yield f'        const result = createFrameMsgInfo();\n\n'
        yield f'        if (buffer.length < {class_name}.OVERHEAD) {{\n'
        yield f'            return result;\n'
        yield f'        }}\n\n'
        
        for i, (sb_name, sb_value) in enumerate(fmt.start_bytes):
            const_name = f'{class_name}.START_BYTE{i + 1}' if len(fmt.start_bytes) > 1 else f'{class_name}.START_BYTE'
            yield f'        if (buffer[{i}] !== {const_name}) {{\n'
            yield f'            return result;\n'
            yield f'        }}\n'
        
        yield f'\n        const msg_length = buffer.length - {class_name}.OVERHEAD;\n\n'
        
        if fmt.has_crc:
            crc_start = len(fmt.start_bytes)
            crc_len = 'msg_length + 1'
            if fmt.has_length:
                crc_len += f' + {fmt.length_bytes}'
            yield f'        const ck = fletcher_checksum(buffer, {crc_start}, {crc_start} + {crc_len});\n'
            yield f'        if (ck[0] === buffer[buffer.length - 2] && ck[1] === buffer[buffer.length - 1]) {{\n'
            yield f'            result.valid = true;\n'
            yield f'            result.msg_id = buffer[{len(fmt.start_bytes)}];\n'
            yield f'            result.msg_len = msg_length;\n'
            yield f'            result.msg_data = new Uint8Array(Array.prototype.slice.call(buffer, {class_name}.HEADER_SIZE, buffer.length - {class_name}.FOOTER_SIZE));\n'
            yield f'        }}\n'
        else:
            yield f'        result.valid = true;\n'
            yield f'        result.msg_id = buffer[{len(fmt.start_bytes)}];\n'
            yield f'        result.msg_len = msg_length;\n'
            yield f'        result.msg_data = new Uint8Array(Array.prototype.slice.call(buffer, {class_name}.HEADER_SIZE));\n'
        
        yield f'\n        return result;\n'
        yield f'    }}\n'
        yield f'}}\n'


